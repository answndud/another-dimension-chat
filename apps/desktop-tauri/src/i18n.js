export const supportedLanguages = Object.freeze(Object.keys(dictionaries));

export function normalizeLanguage(language) {
  return supportedLanguages.includes(language) ? language : "en";
}

export function translate(language, key) {
  const normalized = normalizeLanguage(language);
  return dictionaries[normalized]?.[key] ?? dictionaries.en[key] ?? key;
}

export function hasTranslation(language, key) {
  const normalized = normalizeLanguage(language);
  return Object.prototype.hasOwnProperty.call(dictionaries[normalized] ?? {}, key);
}

export function applyStaticTranslations(root, language) {
  const normalized = normalizeLanguage(language);
  root.documentElement.lang = normalized;
  for (const node of root.querySelectorAll("[data-i18n]")) {
    node.textContent = translate(normalized, node.dataset.i18n);
  }
  for (const node of root.querySelectorAll("[data-i18n-placeholder]")) {
    node.setAttribute("placeholder", translate(normalized, node.dataset.i18nPlaceholder));
  }
  for (const node of root.querySelectorAll("[data-i18n-aria-label]")) {
    node.setAttribute("aria-label", translate(normalized, node.dataset.i18nAriaLabel));
  }
  for (const node of root.querySelectorAll("[data-i18n-title]")) {
    node.setAttribute("title", translate(normalized, node.dataset.i18nTitle));
  }
}
